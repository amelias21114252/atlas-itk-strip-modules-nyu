def get_status(serial: str, parent: str):
    """
    Look up status by serial first, then by parent.
    Falls back to OK if neither is found.
    """
    if serial in status_info:
        return status_info[serial]
    if parent in status_info:
        return status_info[parent]
    return (True, "OK")


